package com.neotelemetrixgdscunand.kamekapp.domain.presentation

import com.neotelemetrixgdscunand.kamekapp.domain.model.BoundingBox
import com.neotelemetrixgdscunand.kamekapp.domain.presentation.ImageDetectorHelper.Companion.CONFIDENCE_THRESHOLD
import com.neotelemetrixgdscunand.kamekapp.domain.presentation.ImageDetectorHelper.Companion.IOU_THRESHOLD

object BoundingBoxProcessor {

    fun getBoundingBox(
        modelOutputFloatArray: FloatArray,
        numElements: Int,
        numChannels: Int,
        labels: List<String>
    ): List<BoundingBox>? {

        val boundingBoxes = mutableListOf<BoundingBox>()

        for (c in 0 until numElements) {
            var maxConf = -1.0f
            var maxIdx = -1
            var j = 4
            var arrayIdx = c + numElements * j
            while (j < numChannels) {
                if (modelOutputFloatArray[arrayIdx] > maxConf) {
                    maxConf = modelOutputFloatArray[arrayIdx]
                    maxIdx = j - 4
                }
                j++
                arrayIdx += numElements
            }

            if (maxConf > CONFIDENCE_THRESHOLD) {
                val clsName = labels[maxIdx]
                val cx = modelOutputFloatArray[c] // 0
                val cy = modelOutputFloatArray[c + numElements] // 1
                val w = modelOutputFloatArray[c + numElements * 2]
                val h = modelOutputFloatArray[c + numElements * 3]
                val x1 = cx - (w / 2F)
                val y1 = cy - (h / 2F)
                val x2 = cx + (w / 2F)
                val y2 = cy + (h / 2F)
                if (x1 < 0F || x1 > 1F) continue
                if (y1 < 0F || y1 > 1F) continue
                if (x2 < 0F || x2 > 1F) continue
                if (y2 < 0F || y2 > 1F) continue

                boundingBoxes.add(
                    BoundingBox(
                        x1 = x1, y1 = y1, x2 = x2, y2 = y2,
                        cx = cx, cy = cy, w = w, h = h,
                        cnf = maxConf, cls = maxIdx, label = clsName
                    )
                )
            }
        }
        if (boundingBoxes.isEmpty()) return null

        return applyNMS(boundingBoxes)
    }


    private fun applyNMS(boxes: List<BoundingBox>): MutableList<BoundingBox> {
        val sortedBoxes = boxes.sortedByDescending { it.cnf }.toMutableList()
        val selectedBoxes = mutableListOf<BoundingBox>()

        while (sortedBoxes.isNotEmpty()) {
            val first = sortedBoxes.first()
            selectedBoxes.add(first)
            sortedBoxes.remove(first)

            val iterator = sortedBoxes.iterator()
            while (iterator.hasNext()) {
                val nextBox = iterator.next()
                val iou = calculateIoU(first, nextBox)
                if (iou >= IOU_THRESHOLD) {
                    iterator.remove()
                }
            }
        }

        return selectedBoxes
    }

    private fun calculateIoU(box1: BoundingBox, box2: BoundingBox): Float {
        val x1 = maxOf(box1.x1, box2.x1)
        val y1 = maxOf(box1.y1, box2.y1)
        val x2 = minOf(box1.x2, box2.x2)
        val y2 = minOf(box1.y2, box2.y2)
        val intersectionArea = maxOf(0F, x2 - x1) * maxOf(0F, y2 - y1)
        val box1Area = box1.w * box1.h
        val box2Area = box2.w * box2.h
        return intersectionArea / (box1Area + box2Area - intersectionArea)
    }

}